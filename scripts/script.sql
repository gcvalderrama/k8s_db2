    COMPRESS NO INCLUDE NULL KEYS ALLOW REVERSE SCANS;

CREATE TABLE "SUTSCHEMA"."SPRING_SESSION_ATTRIBUTES"  (
        "SESSION_ID" CHAR(36 OCTETS) NOT NULL ,
        "ATTRIBUTE_NAME" VARCHAR(200 OCTETS) NOT NULL ,
        "ATTRIBUTE_BYTES" BLOB(1048576) LOGGED NOT COMPACT NOT NULL )
